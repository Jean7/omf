require 'svg2'

#==============================================================================#

DEF_RADIUS = 300

svg = SVG2.new('4in', '4in', '0 0 400 400') {|g|
  g.rect(:x => 20, :x => 20, :width => 160, :height => 160)
  g.rect(:x => 220, :y => 20, :width => 160, :height => 160, :rx => 10, :ry => 10)
  g.circle() { |c|
    c.cx = 100
    c.cy = 300
    c.r = DEF_RADIUS
  }
#  ellipse(300, 300, 80, 50)
}



print svg.to_s

