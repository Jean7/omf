require 'svg/svg'

#==============================================================================#

svg = SVG.new('4in', '4in', '0 0 400 400') { |g|
  g.line(:from => [20, 180], :to => [180, 20])
  g.polyline(:fill => 'none', :stroke => 'black') {|p|
    p.points([220, 20], [220, 180], [300, 20], [300, 180])
    p.points([380, 20], [380, 180])
  }
  g.polygon(:points => [[20, 380], [100, 220], [180, 380]])
  g.image(:x => 200, :y => 200, :width => 200, :height => 40, 
          :href => 'http://yuya.4th.to/banner.gif')
  g.text(:x => 200, :y => 300, :text => 'Hello! world.')
}
print svg.to_s

